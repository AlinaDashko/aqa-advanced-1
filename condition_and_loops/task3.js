const number = 10;
console.log("Таблиця множення за допомогою цикла for");
for (let i = 1; i <= 10; i++) {
    const result = number * i;
    console.log(`${number} * ${i} = ${result}`);
}

console.log("Таблиця множення за допомогою цикла while");
let a = 1;
while (a <= 10) {
    let result = number * a;
    console.log(number + " * " + a + " = " + result);
    a++;
}







